# using arrays 

my_array = [1, 2, 3, 4, 5]

# add element to the end 
my_array.append(6)

# remove the last element
my_array.pop()

print(my_array)

sum_of_elements = sum(my_array)
print(sum_of_elements)

length_of_array = len(my_array)
print(length_of_array)

min_element = min(my_array)
print(min_element)

max_element = max(my_array)
print(max_element)

# sort the array in ascending order
sorted_array = sorted(my_array)
print(sorted_array)

reversed_array = list(reversed(my_array))
print(reversed_array)
